import random

rock = '''
    _______
---'   ____)
      (_____)
      (_____)
      (____)
---.__(___)
'''

paper = '''
    _______
---'   ____)____
          ______)
          _______)
         _______)
---.__________)
'''

scissors = '''
    _______
---'   ____)____
          ______)
       __________)
      (____)
---.__(___)
'''

game_images = [rock, paper, scissors]

n = int(input("0 for Rock, 1 for Paper, 2 for Scissors \n"))

if n >= 3 or n < 0:
  print("You entered an invalid number, try again!")

print("User chose: " + game_images[n])

comp = random.randint(0,2)

print("Computer chose: " + game_images[comp])

if n == comp:
    print("You tied!")
elif n == 0 and comp == 2:
    print("You win!")
elif comp == 0 and n == 2:
    print("You lose!")
elif comp > n:
    print("You lose!")
elif n > comp:
    print("You win!")

